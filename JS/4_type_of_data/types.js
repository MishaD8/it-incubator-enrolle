// Динамическая типизация
/* 
JavaScript является динамическим типизированным. То есть тип данных переменной меняется 
динамически в момент присвоения либо смены ее значения. А не в момент объявления.
*/

let userName; //Объявляем переменную 

console.log(typeof userName);

userName = "freelance for live"; // строка (String)

console.log(typeof userName);

userName = 58; //Число (Number)

console.log(typeof userName);

